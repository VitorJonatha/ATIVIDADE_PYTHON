mercurio = 'Mercúrio'
venus = 'Vênus'
terra = 'Terra'
marte = 'Marte'
jupiter = 'Júpiter'
saturno = 'Saturno'
urano = "Urano"
netuno = "Netuno"
nome = str(input("Digite o nome de um planeta:"))

if nome == mercurio:
    print("Planeta Mercúrio!")
elif nome == venus:
    print("Planeta Vênus!")
elif nome == terra:
    print("Planeta Terra!")
elif nome == marte:
    print("Planeta Marte!")
elif nome == jupiter:
    print("Planeta Júpiter!")
elif nome == saturno:
    print("Planeta Satuno!")
elif nome == urano:
    print("Planeta Urano!")
elif nome == netuno:
    print("Planeta Netuno!")
else:
    print("Nenhum planeta.")
